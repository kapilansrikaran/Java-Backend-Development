package operators;

public class AssignmentVsEqualityOperatorsExample {
    public static void main(String[] args) {
        int x = 5;
        // x is assigned value 5

        float y = 2.3F;
        // y is assigned value 2.3


        System.out.println(x == 5);
        System.out.println(x == 2);
    }
}
