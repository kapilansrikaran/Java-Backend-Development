package operators;

public class RelationalOperatorsExample {
    public static void main(String[] args) {
        int a=10, b = 5;

//        == Operator
        System.out.println(a==b);

//        != Operator
        System.out.println(a!=b);

//        > Operator
        System.out.println(a>b);

//        < Operator
        System.out.println(a<b);

//        >= Operator
        System.out.println(a >= b);

//        <= Operator
        System.out.println(a<= b);

    }
}
