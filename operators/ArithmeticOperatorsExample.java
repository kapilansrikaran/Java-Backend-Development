package operators;

public class ArithmeticOperatorsExample {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

//        Addition
        System.out.println(a+b);

//        Subtraction
        System.out.println(a-b);

//        Multiplication
        System.out.println(a*b);

//        Division
        System.out.println(a/b);
        System.out.println(a/ (float)b);

//        Modula Operator - Reminder
        System.out.println(a%b);
    }
}
